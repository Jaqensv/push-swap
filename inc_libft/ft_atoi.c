#include "libft.h"

long long int	ft_atoi(const char *str)
{
	int				i;
	int				booleen;
	long long int	n;

	i = 0;
	booleen = 1;
	n = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			booleen *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * booleen);
}
